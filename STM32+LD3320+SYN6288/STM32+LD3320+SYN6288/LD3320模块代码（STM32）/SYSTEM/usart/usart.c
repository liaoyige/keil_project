void USART3_SendData(uint8_t data)
{
	while((USART3->SR & 0X40) == 0);
	USART3->DR = data;
}

void USART3_SendString(uint8_t *DAT, uint8_t len)
{
		uint8_t i;
		for(i = 0; i < len; i++)
		{
				USART3_SendData(*DAT++);
		}
}

